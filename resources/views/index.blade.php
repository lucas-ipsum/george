<h1> Herzlich Willkommen in der Wirtschaftsinformatik </h1>
<p> Die Professur für Anwendungssysteme und E-Business beschäftigt sich mit der Gestaltung moderner betrieblicher Anwendungssysteme, dem Einführen dieser Systeme sowie dem Betrieb. Dabei interessieren insbesondere auch die Nutzeffekte, die mit den IT-basierten Lösungen verbunden sind. Bei der Gestaltung von betrieblichen oder zwischenbetrieblichen Anwendungssystemen geht es um die Frage, wie mit neuen Algorithmen, neuen technischen Möglichkeiten, veränderten Prozessen, veränderter Integration oder einem neuen organisatorischen Verbund Lösungen verbessert oder gegenüber der vorhandenen Situation sinnvoll weiter automatisiert oder ganz neue Lösungen oder Geschäftsmodelle angeboten werden können. <a href="http://www.uni-goettingen.de/de/44695.html"> mehr... </a>
